def load_stock(symbol:str):
    try:
        data = yf.download(symbol, start=day30, end=today)
        df_data = pd.DataFrame(data)
        df_data = df_data.drop("Adj Close", axis=1)
        df_data = df_data.diff()
        df_data = df_data.dropna()
        return df_data, True
    except ValueError:
        print("Skipping {}".format(symbol))
        return None, False

